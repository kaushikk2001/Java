public class SwitchBreak {
    public static void main(String[] args) {
        int Button=2;
        switch(Button)
        {
            case 1:System.out.println("Button 1 pressed");//Case 1
            break;
            case 2:System.out.println("Button 2 pressed");//case 2
            break;
            default:System.out.println("No button Pressent");//default case
        }
    }
}
